public class Urna {
    private int colegio;
    private int mesa;

    public Urna(int colegio, int mesa) {
        this.colegio = colegio;
        this.mesa = mesa;
    }

    public int getColegio() {
        return colegio;
    }

    public int getMesa() {
        return mesa;
    }
}